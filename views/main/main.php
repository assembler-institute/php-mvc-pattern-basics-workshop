  <h2>WELCOME TO THE MVC PATTERN BASICS PRACTICE WITH PHP</h2>
  <article>
    <img src="https://upload.wikimedia.org/wikipedia/commons/thumb/a/a0/MVC-Process.svg/500px-MVC-Process.svg.png" style="float: right;">
    <p>Model–view–controller (usually known as MVC) is a software design pattern commonly used for developing user interfaces that divide the related program logic into three interconnected elements. This is done to separate internal representations of information from the ways information is presented to and accepted from the user.</p>

    <p>Traditionally used for desktop graphical user interfaces (GUIs), this pattern has become popular for designing web applications. Popular programming languages have MVC frameworks that facilitate implementation of the pattern.</p>
    <h3>COMPLONENTS</h3>
    <br />
    <h4>Model</h4>
    <p>The central component of the pattern. It is the application's dynamic data structure, independent of the user interface. It directly manages the data, logic and rules of the application.</p>
    <h4>View</h4>
    <p>Any representation of information such as a chart, diagram or table. Multiple views of the same information are possible, such as a bar chart for management and a tabular view for accountants.</p>
    <h4>Controller</h4>
    <p>Accepts input and converts it to commands for the model or view.</p>
    <p>In addition to dividing the application into these components, the model–view–controller design defines the interactions between them.</p>
    <p>
    <ul style="list-style: square; margin-left: 2rem">
      <li>The model is responsible for managing the data of the application. It receives user input from the controller.</li>
      <li>The view renders presentation of the model in a particular format.</li>
      <li>The controller responds to the user input and performs interactions on the data model objects. The controller receives the input, optionally validates it and then passes the input to the model.</li>
    </ul>
    </p>
    <p>As with other software patterns, MVC expresses the "core of the solution" to a problem while allowing it to be adapted for each system. Particular MVC designs can vary significantly from the traditional description here.</p>
