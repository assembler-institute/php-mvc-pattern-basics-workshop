-- Database creation
DROP DATABASE IF EXISTS mvc_basics;
CREATE DATABASE IF NOT EXISTS mvc_basics;
USE mvc_basics;

-- Creation of the tables
CREATE TABLE types (
    id INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(25) NOT NULL,
    avatar VARCHAR(200)
);

CREATE TABLE pokemon (
    id INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(50) NOT NULL,
    avatar VARCHAR(200),
    altura FLOAT,
    peso FLOAT,
    categoria VARCHAR(50)
);

CREATE TABLE pokemon_types (
    poke_id INT NOT NULL,
    type_id INT NOT NULL,
    FOREIGN KEY (poke_id) REFERENCES pokemon(id),
    FOREIGN KEY (type_id) REFERENCES types(id)
);

CREATE TABLE efect_types (
    typeAttack INT,
    typeDefense INT,
    effect FLOAT,
    FOREIGN KEY (typeAttack) REFERENCES types(id),
    FOREIGN KEY (typeDefense) REFERENCES types(id)
);

